class Solution:
    def coloredCells(self, n: int) -> int:
        # 매번 상하좌우는 4개씩 늘어나고, 대각선 쪽이 각각 1개씩 추가로 4개가 늘어난다.
        # 1 -> 5 -> 13 -> 25
        # 1 -> 1 + 4*1 -> 1 + 4*1 + 4*2 -> 1 + 4*1 + 4*2 + 4*3
        # n => 1 + 4*1 + 4*2 + ... + 4*(n-1) == 1 + 4 * (1 + 2 + ... + n - 1) == 1 + 4 * (n * (n - 1)) // 2 == 1 + 2 * (n * n - 1)
        return 1 + 2 * n * (n - 1)